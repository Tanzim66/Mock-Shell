package commands;

import driver.JShell;

/**
 * The Commands class is the super class of all commands. All child classes use
 * the method in this class to execute their own commands.
 */
public abstract class Commands {

  /**
   * This method obtains user input as well as informations stored in JShell
   * that are essential for executing each command.
   * 
   * @param js - JShell which stores the locations of root directory and current
   *        directory, the directory stack, a list of recent user inputs.
   * @param cmdAndParam - the user input
   * @return the output of the command or a string that indicates whether the
   *         execution of command was a success or a failure
   */
  public abstract String executeCommand(JShell js, String[] cmdAndParam);
}
